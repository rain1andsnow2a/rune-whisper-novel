export const getChapterById = (id: number): Chapter | undefined => {
  return chapters.find(chapter => chapter.id === id);
};

export const getNextChapterId = (currentChapterId: number): number | null => {
  const currentIndex = chapters.findIndex(chapter => chapter.id === currentChapterId);
  if (currentIndex < chapters.length - 1) {
    return chapters[currentIndex + 1].id;
  }
  return null;
};

export const getPrevChapterId = (currentChapterId: number): number | null => {
  const currentIndex = chapters.findIndex(chapter => chapter.id === currentChapterId);
  if (currentIndex > 0) {
    return chapters[currentIndex - 1].id;
  }
  return null;
};